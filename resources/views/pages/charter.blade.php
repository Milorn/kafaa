        <div class="max-w-screen-lg mx-auto my-20">
            <h1 class="text-2xl font-bold text-primary mb-6">
                Charte d'Utilisation de la Plateforme de Candidature pour le Label d'Installateur d'Équipements
                Photovoltaïques ou d'Éclairage Public
            </h1>

            <h2 class="font-semibold">Préambule:</h2>
            <p>
                La présente charte d'utilisation a pour objet de définir les règles et bonnes pratiques à respecter par
                les
                utilisateurs de la plateforme (ci-après dénommée « la Plateforme ») dédiée à la candidature pour
                l'obtention du label d'installateur d'équipements photovoltaïques ou d'éclairage public.
                Tout utilisateur de la Plateforme s'engage à respecter les dispositions de la présente charte, sous
                peine
                de sanctions pouvant aller jusqu'à la suspension ou la suppression de son compte.
            </p>

            <h2 class="font-semibold mt-4">1. Accès à la Plateforme:</h2>

            <p>
                L'accès à la Plateforme est réservé aux professionnels du secteur photovoltaïque ou d'éclairage public
                souhaitant obtenir le label d'installateur. Chaque utilisateur doit s'inscrire en fournissant des
                informations exactes et vérifiables, et est tenu de les mettre à jour si nécessaire.
            </p>

            <h2 class="font-semibold mt-4"> 2. Conditions d'Inscription:</h2>

            <p>
                Lors de l'inscription, l'utilisateur devra fournir : <br>

                - Les informations légales de son entreprise (adresse, etc.) <br>
                - Un historique de ses installations antérieures <br>
                - Les certifications ou qualifications techniques en rapport avec le métier (par exemple, agrément…)
                <br>
                - Les informations soumises seront soumises à vérification par un comité de validation.
            </p>

            <h2 class="font-semibold mt-4">3. Utilisation Responsable de la Plateforme:</h2>

            <p>
                L'utilisateur s'engage à : <br>
                - Utiliser la Plateforme uniquement dans le cadre de sa démarche de candidature pour l'obtention 
                du label <br>
                - Respecter la confidentialité des informations partagées sur la Plateforme <br>
                - Ne pas diffuser de fausses informations, ni usurper l'identité d'une autre entreprise ou d'une 
                autre personne <br>
                - Respecter les droits de propriété intellectuelle de la Plateforme
            </p>

            <h2 class="font-semibold mt-4">4. Propriété Intellectuelle:</h2>

            <p>
                Les contenus, logos, textes, éléments graphiques, et autres ressources présents sur la Plateforme sont
                protégés par des droits d'auteur. Toute reproduction, diffusion ou utilisation non autorisée de ces
                éléments est interdite.
            </p>

            <h2 class="font-semibold mt-4">5. Soumission de Candidature:</h2>

            <p>
                Les utilisateurs doivent remplir un formulaire de candidature en ligne, téléverser les documents requis,
                et fournir toute information complémentaire demandée par le comité de validation. Toute soumission
                frauduleuse, incomplète ou inexacte pourra entraîner le rejet de la candidature.
            </p>

            <h2 class="font-semibold mt-4">6. Protection des Données Personnelles:</h2>

            <p>
                Les informations personnelles et professionnelles collectées sont destinées uniquement au traitement
                des candidatures pour le label. Elles sont conservées de manière sécurisée et ne seront partagées avec
                des tiers qu'avec le consentement exprès de l'utilisateur, sauf si la loi l'exige.
            </p>

            <h2 class="font-semibold mt-4">7. Confidentialité des Échanges:</h2>

            <p>
                Tous les échanges et documents partagés sur la Plateforme dans le cadre de la candidature sont
                confidentiels. L'utilisateur s'engage à ne pas divulguer à des tiers des informations sensibles
                concernant
                sa candidature ou celle d'autres utilisateurs.
            </p>

            <h2 class="font-semibold mt-4">8. Respect de la Règlementation:</h2>

            <p>
                L'utilisateur s'engage à respecter les règlementations nationales et locales en vigueur relatives aux
                installations photovoltaïques et d'éclairage public. Il devra également s'assurer que son entreprise
                dispose de toutes les assurances et certifications nécessaires pour exercer légalement son métier.
            </p>

            <h2 class="font-semibold mt-4">9. Responsabilité de l'Utilisateur:</h2>

            <p>
                L'utilisateur est seul responsable des informations et documents qu'il soumet sur la Plateforme. Il
                garantit que toutes les informations sont exactes et conformes à la réalité. En cas de fraude ou de
                fausse
                déclaration, l'utilisateur pourrait voir sa candidature rejetée et ses accès à la Plateforme suspendus.
            </p>

            <h2 class="font-semibold mt-4">10. Modification de la Charte:</h2>

            <p>
                La présente charte peut être modifiée à tout moment par les administrateurs de la Plateforme. Les
                utilisateurs seront informés de toute modification via une notification sur leur compte. L'utilisation
                continue de la Plateforme après ces modifications vaut acceptation de la nouvelle charte.
            </p>

            <h2 class="font-semibold mt-4">11. Sanctions en Cas de Non-Respect:</h2>

            <p>
                En cas de non-respect des règles énoncées dans la présente charte, l'utilisateur peut être sanctionné,
                notamment par : <br>
                - Un avertissement <br>
                - La suspension temporaire ou définitive de son compte <br>
                - Le rejet de sa candidature pour le label
            </p>

            <h2 class="font-semibold mt-4">12. Contact et Support:</h2>

            <p>
                Pour toute question relative à l'utilisation de la Plateforme ou à la candidature pour le label, les
                utilisateurs peuvent contacter l'équipe support via l'adresse : <span class="font-semibold">contact@cerefe.gov.dz</span>
            </p>
        </div>

    </x-slot:main>
